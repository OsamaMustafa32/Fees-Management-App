    void fillEntries() {
        try {
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) atable.getModel();

            ResultSet rs = db.DbConnect.getaccountant.executeQuery();
            while (rs.next()) {
                Vector r = new Vector();
                r.add(rs.getString("username"));
                r.add(rs.getString("pass"));
                r.add(rs.getString("email"));
                r.add(rs.getString("contact"));
                dtm.addRow(r);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

