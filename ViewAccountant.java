    private void atableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_atableMouseClicked
        // SELECT A ROW CODE:
        int ri = atable.getSelectedRow();
        if (ri != -1) {
            String username = (String) atable.getValueAt(ri, 0);
            String pass = (String) atable.getValueAt(ri, 1);
            String email = (String) atable.getValueAt(ri, 2);
            String contact = (String) atable.getValueAt(ri, 3);
            
            n.setText(username);
            p.setText(pass);
            e.setText(email);
            c.setText(contact);
            
        }

    }//GEN-LAST:event_atableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      // UPDATING ACOUNTANT
      
        try {
            String username = n.getText();
            String pass = String.valueOf(p.getPassword());
            String email = e.getText();
            String contact = c.getText();
            int r=atable.getSelectedRow();
            if (!username.equals("") && !pass.equals("") && !email.equals("") && !c.getText().equals("")) {
                db.DbConnect.updateaccountant.setString(1, username);
                db.DbConnect.updateaccountant.setString(2, pass);
                db.DbConnect.updateaccountant.setString(3, email);
                db.DbConnect.updateaccountant.setString(4, contact);
                db.DbConnect.updateaccountant.setString(5,(String) atable.getValueAt(r, 0));
                int x=db.DbConnect.updateaccountant.executeUpdate();
                if(x==1){
                    JOptionPane.showMessageDialog(null,"updated successfully");
                    new ViewAccountant().setVisible(true);
                    this.dispose();
                }
                atableMouseClicked(null);
                
                
                n.setText(null);
                p.setText(null);
                e.setText(null);
                c.setText(null);
            } else {
                JOptionPane.showMessageDialog(null, "Please fill Complete Form");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

